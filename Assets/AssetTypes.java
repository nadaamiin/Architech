package Assets;

/**
 * Enum representing the different types of assets.
 * This enum is used to categorize various types of assets such as stocks, real estate, cryptocurrency, and gold.
 */
public enum AssetTypes {
    /**
     * Represents a stock asset type.
     */
    STOCK,

    /**
     * Represents a real estate asset type.
     */
    REAL_ESTATE,

    /**
     * Represents a cryptocurrency asset type.
     */
    CRYPTO,

    /**
     * Represents a gold asset type.
     */
    GOLD
}
